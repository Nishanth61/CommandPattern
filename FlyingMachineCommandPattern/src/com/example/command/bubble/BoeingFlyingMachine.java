package com.example.command.bubble;

/**
 * 
 * Boeing Flying Machine
 *
 */
public class BoeingFlyingMachine {

	public BoeingFlyingMachine() {

	}

	/**
	 * Engine Start of Flying Machine
	 */
	public void engineStart() {
		System.out.println("BoeingFlyingMachine Started");
	}

	/**
	 * Landing of Flying Machine
	 */
	public void landing() {
		System.out.println("BoeingFlyingMachine Landed");

	}

	/**
	 * Take Off of Flying Machine
	 */
	public void takeOff() {
		System.out.println("BoeingFlyingMachine takeOff");

	}

	/**
	 * Engine Stop of Flying Machine
	 */
	public void engineStop() {
		System.out.println("BoeingFlyingMachine Stopped");

	}

}
