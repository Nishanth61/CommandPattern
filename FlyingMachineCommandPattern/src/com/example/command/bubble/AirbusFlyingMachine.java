package com.example.command.bubble;
/**
 * AirbusFlyingMachine
 *
 */
public class AirbusFlyingMachine {

	public AirbusFlyingMachine() {

	}
	/**
	 * Engine Start of Flying Machine
	 */
	public void engineStart() {
		System.out.println("AirbusFlyingMachine Started");
	}
	
	/**
	 * Landing of Flying Machine
	 */
	public void landing() {
		System.out.println("AirbusFlyingMachine Landed");

	}
	/**
	 * Take Off of Flying Machine
	 */
	public void takeOff() {
		System.out.println("AirbusFlyingMachine takeOff");

	}
	/**
	 * Engine Stop of Flying Machine
	 */
	public void engineStop() {
		System.out.println("AirbusFlyingMachine Stopped");

	}

}
